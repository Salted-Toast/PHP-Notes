<?php
    // echo - Outputs Str, Int, Real, html etc
    echo "This is a string", 32, 32.32;

    // print - Same as echo but only takes single value
    print "This is a string";

    // print_r() - Prints single values and arrays
    print_r([1,2,3]);

    // var_dump() - returns more information like data type and length
    var_dump(true);
    
    // var_export() - like var_dump() outputs a str representation of a variable
    var_export(123);

?>